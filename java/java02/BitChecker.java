public class BitChecker {
    public static void main(String[] argv) {
        int mask = 0b1;

        System.out.println(mask);
        mask <<= 1;  // mask = mask << 1;
        System.out.println(mask);
        mask <<= 1;  // mask = mask << 1;
        System.out.println(mask);
        mask <<= 1;  // mask = mask << 1;
        System.out.println(mask);
    }
    
}
